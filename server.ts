// Save practice test score
app.post("/api/student/practice", (req, res) => {
  const { subject, score, totalQuestions } = req.body;
  if (!subject || score === undefined || !totalQuestions) {
    res.status(400).json({ error: "Missing required fields" });
    return;
  }
  const newAttempt: PracticeAttempt = {
    id: String(Date.now()),
    subject,
    score: Number(score),
    totalQuestions: Number(totalQuestions),
    timestamp: new Date().toISOString(),
  };
  studentDatabase.attempts.push(newAttempt);

  // Recalculate predicted score based on last active attempts
  const totalCorrect = studentDatabase.attempts.reduce((acc, sub) => acc + sub.score, 0);
  const totalAsked = studentDatabase.attempts.reduce((acc, sub) => acc + sub.totalQuestions, 0);
  if (totalAsked > 0) {
    studentDatabase.predictedScore = Math.round((totalCorrect / totalAsked) * 100);
  }

  res.json({ success: true, updatedAttempts: studentDatabase.attempts, predictedScore: studentDatabase.predictedScore });
});

// Save notes generated by AI
app.post("/api/student/notes", (req, res) => {
  const { title, content, subject } = req.body;
  if (!title || !content || !subject) {
    res.status(400).json({ error: "Missing required fields" });
    return;
  }
  const newNote: SavedNote = {
    id: "note-" + Date.now(),
    title,
    content,
    subject,
    timestamp: new Date().toISOString()
  };
  studentDatabase.notes.unshift(newNote);
  res.json({ success: true, note: newNote, allNotes: studentDatabase.notes });
});

// Delete saved note
app.delete("/api/student/notes/:id", (req, res) => {
  const { id } = req.params;
  const index = studentDatabase.notes.findIndex(n => n.id === id);
  if (index !== -1) {
    studentDatabase.notes.splice(index, 1);
    res.json({ success: true, allNotes: studentDatabase.notes });
  } else {
    res.status(404).json({ error: "Note not found" });
  }
});

