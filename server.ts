// 🤖 AI exam generator (returns structured JSON questions)
app.post("/api/gemini/quiz", async (req, res) => {
  try {
    const { subject, topic, level, numQuestions } = req.body;
    if (!subject) {
      res.status(400).json({ error: "Subject is required" });
      return;
    }

    const ai = getGenAI();
    const count = numQuestions || 5;
    const requestedTopic = topic || "General Concepts";
    
    const prompt = `Create a high-quality assessment quiz of exactly ${count} Multiple Choice Questions (MCQs) for the subject "${subject}" specifically on the topic "${requestedTopic}". The difficulty level should be "${level || "Medium"}". Provide accurate, logical options.`;

    const response = await ai.models.generateContent({
      model: "gemini-3.5-flash",
      contents: prompt,
      config: {
        systemInstruction: "You are a professional examiners board officer generating clean academic test questions. Each question must have exactly 4 logical options and a clear explanation detailing why the correctAnswer is correct.",
        responseMimeType: "application/json",
        responseSchema: {
          type: Type.OBJECT,
          properties: {
            questions: {
              type: Type.ARRAY,
              description: `List of exactly ${count} multiple choice questions.`,
              items: {
                type: Type.OBJECT,
                properties: {
                  question: { type: Type.STRING },
                  options: {
                    type: Type.ARRAY,
                    items: { type: Type.STRING },
                    description: "Exactly 4 options."
                  },
                  correctAnswer: { 
                    type: Type.STRING, 
                    description: "The exact correct option string. Must match one of the items inside 'options' array exactly." 
                  },
                  explanation: { type: Type.STRING, description: "Detailed educational reasoning behind the correct option." }
                },
                required: ["question", "options", "correctAnswer", "explanation"]
              }
            }
          },
          required: ["questions"]
        }
      }
    });

    const parsedJson = JSON.parse(response.text || "{}");
    res.json(parsedJson);
  } catch (error: any) {
    console.error("Error in AI Exam Generator API:", error);
    res.status(500).json({ error: error.message || "An error occurred during quiz generation." });
  }
});


// Serve React Frontend (Vite Setup)
async function startServer() {
  if (process.env.NODE_ENV !== "production") {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    const distPath = path.join(process.cwd(), "dist");
    app.use(express.static(distPath));
    app.get("*", (req, res) => {
      res.sendFile(path.join(distPath, "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`MRIVAN AI full-stack server running on http://localhost:${PORT}`);
  });
}

startServer();
